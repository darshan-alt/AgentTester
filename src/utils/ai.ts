export async function evaluateAnswer(
  question: string,
  answer: string,
  settings: Settings
): Promise<EvaluationResponse> {
  const { provider, model, systemPrompt, apiKeys } = settings;
  const apiKey = apiKeys[provider];

  if (!apiKey) {
    throw new Error(`API Key for ${provider} is missing. Please set it in Settings.`);
  }

  const promptMessage = `Question:\n${question}\n\nAnswer:\n${answer}`;

  try {
    let jsonResponse = '';

    if (provider === 'openai') {
      jsonResponse = await callOpenAI(apiKey, model, systemPrompt, promptMessage);
    } else if (provider === 'anthropic') {
      jsonResponse = await callAnthropic(apiKey, model, systemPrompt, promptMessage);
    } else if (provider === 'gemini') {
      jsonResponse = await callGemini(apiKey, model, systemPrompt, promptMessage);
    }

    const cleanJson = jsonResponse.replace(/```json/g, '').replace(/```/g, '').trim();
    return JSON.parse(cleanJson) as EvaluationResponse;
  } catch (error) {
    console.error('Evaluation Error:', error);
    throw new Error(`Failed to evaluate. Ensure API keys are correct and try again. Details: ${(error as Error).message}`);
  }
}

async function callOpenAI(apiKey: string, model: string, system: string, userMsg: string): Promise<string> {
  const res = await fetch('https://api.openai.com/v1/chat/completions', {
    method: 'POST',
    headers: {
      'Content-Type': 'application/json',
      'Authorization': `Bearer ${apiKey}`
    },
    body: JSON.stringify({
      model,
      messages: [
        { role: 'system', content: system },
        { role: 'user', content: userMsg }
      ],
      response_format: { type: 'json_object' }
    })
  });

  if (!res.ok) {
    const err = await res.json();
    throw new Error(err.error?.message || res.statusText);
  }

  const data = await res.json();
  return data.choices[0].message.content;
}

async function callAnthropic(apiKey: string, model: string, system: string, userMsg: string): Promise<string> {
  const res = await fetch('https://api.anthropic.com/v1/messages', {
    method: 'POST',
    headers: {
      'Content-Type': 'application/json',
      'x-api-key': apiKey,
      'anthropic-version': '2023-06-01',
      'anthropic-dangerous-direct-browser-access': 'true'
    },
    body: JSON.stringify({
      model,
      max_tokens: 4096,
      system,
      messages: [
        { role: 'user', content: userMsg + '\n\nPlease return strictly valid JSON matching the requested structure.' }
      ]
    })
  });

  if (!res.ok) {
    const err = await res.json();
    throw new Error(err.error?.message || res.statusText);
  }

  const data = await res.json();
  return data.content[0].text;
}

async function callGemini(apiKey: string, model: string, system: string, userMsg: string): Promise<string> {
  const res = await fetch(`https://generativelanguage.googleapis.com/v1beta/models/${model}:generateContent?key=${apiKey}`, {
    method: 'POST',
    headers: {
      'Content-Type': 'application/json'
    },
    body: JSON.stringify({
      system_instruction: { parts: [{ text: system }] },
      contents: [
        { role: 'user', parts: [{ text: userMsg + '\n\nPlease return strictly valid JSON matching the requested structure.' }] }
      ],
      generationConfig: {
        responseMimeType: "application/json",
      }
    })
  });

  if (!res.ok) {
    const err = await res.json();
    throw new Error(err.error?.message || res.statusText);
  }

  const data = await res.json();
  return data.candidates[0].content.parts[0].text;
}
